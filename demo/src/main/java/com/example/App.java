package com.example;

/**
 * ================================================================
 *  DATA STRUCTURES VISUALIZER — Wave-Based Practice Project
 * ================================================================
 *
 *  Welcome! This project walks you through 7 classic data structures,
 *  each broken into 4 progressive difficulty waves.
 *
 *  STRUCTURE:
 *    Each data structure lives in its own file under src/main/java/com/example/
 *    Each has a matching test file under src/test/java/com/example/
 *
 *  DATA STRUCTURES:
 *    1. SinglyLinkedList.java
 *    2. DoublyLinkedList.java
 *    3. MyStack.java
 *    4. MyQueue.java
 *    5. BinarySearchTree.java
 *    6. Graph.java
 *    7. MyHashMap.java
 *
 *  THE 4 WAVES (per structure):
 *    Wave 1 – Intro       : Core operations — get the structure standing
 *    Wave 2 – Basic       : Add, remove, and inspect
 *    Wave 3 – Intermediate: Traversals, precise insertion, and searches
 *    Wave 4 – Advanced    : Classic interview algorithms
 *
 *  HOW TO USE:
 *    1. Open a data structure file (e.g. SinglyLinkedList.java)
 *    2. Read the description at the top of the file
 *    3. Find the first TODO comment in Wave 1 and implement it
 *    4. Run the matching test: mvn test -Dtest=SinglyLinkedListTest
 *    5. Fix failures, then move on to the next TODO
 *    6. Repeat for each wave until all tests pass
 *
 *  RUN ALL TESTS:
 *    mvn test
 *
 *  RUN ONE STRUCTURE'S TESTS:
 *    mvn test -Dtest=SinglyLinkedListTest
 *    mvn test -Dtest=DoublyLinkedListTest
 *    mvn test -Dtest=MyStackTest
 *    mvn test -Dtest=MyQueueTest
 *    mvn test -Dtest=BinarySearchTreeTest
 *    mvn test -Dtest=GraphTest
 *    mvn test -Dtest=MyHashMapTest
 * ================================================================
 */
public class App {
    public static void main(String[] args) {
        System.out.println("==============================================");
        System.out.println(" Data Structures Visualizer — Wave Practice");
        System.out.println("==============================================");
        System.out.println();
        System.out.println("Open any data structure file to begin.");
        System.out.println("Run:  mvn test   to check your progress.");
    }
}
